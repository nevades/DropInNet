// toggle full screen
function toggleFullScreen() {
    var a = $(window).height() - 10;
    if (!document.fullscreenElement && // alternative standard method
            !document.mozFullScreenElement && !document.webkitFullscreenElement) { // current working methods
        if (document.documentElement.requestFullscreen) {
            document.documentElement.requestFullscreen();
        } else if (document.documentElement.mozRequestFullScreen) {
            document.documentElement.mozRequestFullScreen();
        } else if (document.documentElement.webkitRequestFullscreen) {
            document.documentElement.webkitRequestFullscreen(Element.ALLOW_KEYBOARD_INPUT);
        }
    } else {
        if (document.cancelFullScreen) {
            document.cancelFullScreen();
        } else if (document.mozCancelFullScreen) {
            document.mozCancelFullScreen();
        } else if (document.webkitCancelFullScreen) {
            document.webkitCancelFullScreen();
        }
    }
    $('.full-screen').toggleClass('icon-maximize');
    $('.full-screen').toggleClass('icon-minimize');
}

function setTableStatus(td, custom, whiteSpace) {

    let whiteSpaceTxt = 'style="white-space: nowrap"';
    if (typeof whiteSpace !== 'undefined' && !whiteSpace) {
        whiteSpaceTxt = "";
    }

    if ($(td).html().trim().startsWith('active')) {
        $(td).html('<label class="label label-success" ' + whiteSpaceTxt + '>' + $(td).html().trim() + '</label>');
    } else if ($(td).html().trim().startsWith('pending')) {
        $(td).html('<label class="label label-warning" ' + whiteSpaceTxt + '>' + $(td).html().trim() + '</label>');
    } else if ($(td).html() === 'rejected') {
        $(td).html('<label class="label label-danger" ' + whiteSpaceTxt + '>Rejected</label>');
    } else if ($(td).html() === 'deactivated') {
        $(td).html('<label class="label label-inverse" ' + whiteSpaceTxt + '>Deactivated</label>');
    } else if ($(td).html() === 'in progress') {
        $(td).html('<label class="label label-info" ' + whiteSpaceTxt + '>In Progress</label>');
    } else if (custom) {
        for (let i = 0; i < custom.length; i++) {
            if (custom[i].status === $(td).html().trim()) {
                $(td).html('<label class="label ' + custom[i].class + '" ' + (typeof custom[i].whiteSpace !== 'undefined' && !custom[i].whiteSpace ? '' : 'style="white-space: nowrap"') + '>' + $(td).html().trim() + '</label>');
                return;
            }
        }
    }
}